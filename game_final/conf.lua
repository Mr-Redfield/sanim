function love.conf(t)--função de configurações love
	t.window.incon = "imagens/dragon.png"--define um icone
	t.window.title = " World Scale "--nome do arquivo
	t.window.width = 800--resolução vertical
	t.window.heigh = 600--resolução horizontal
	t.modules.touch = false--moludos de toque
	t.window.fullscreen = false--define a tela cheia
	t.window.icon = "imagens/dragon.png"--cria um icône para o game.
end